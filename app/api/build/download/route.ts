import { createServerClient } from "@supabase/ssr";
import { cookies } from "next/headers";
import { NextRequest, NextResponse } from "next/server";
import JSZip from "jszip";

export async function POST(req: NextRequest) {
  const cookieStore = cookies();
  const supabase = createServerClient(
    process.env.NEXT_PUBLIC_SUPABASE_URL!,
    process.env.NEXT_PUBLIC_SUPABASE_ANON_KEY!,
    {
      cookies: {
        getAll() { return cookieStore.getAll(); },
        setAll(cookiesToSet) {
          cookiesToSet.forEach(({ name, value, options }) =>
            cookieStore.set(name, value, options)
          );
        },
      },
    }
  );

  const { data: { user } } = await supabase.auth.getUser();
  if (!user) return NextResponse.json({ error: "Unauthorized" }, { status: 401 });

  const { files, productName } = await req.json();
  if (!files || !Array.isArray(files))
    return NextResponse.json({ error: "Files required" }, { status: 400 });

  const zip = new JSZip();
  const folderName = (productName || "project").toLowerCase().replace(/\s+/g, "-");
  const root = zip.folder(folderName)!;

  for (const file of files as { path: string; code: string }[]) {
    const parts = file.path.split("/");
    if (parts.length === 1) {
      root.file(file.path, file.code);
    } else {
      let currentFolder = root;
      for (let i = 0; i < parts.length - 1; i++) {
        currentFolder = currentFolder.folder(parts[i])!;
      }
      currentFolder.file(parts[parts.length - 1], file.code);
    }
  }

  root.file(
    "HACKER.md",
    `# Generated by Hacker ⚡\n\nThis project was generated by **Hacker**, the AI code agent by [Arteno](https://arteno.in).\n\n## Product: ${productName}\n\n## Getting Started\n\n\`\`\`bash\nnpm install\ncp .env.example .env.local\n# Fill in your environment variables\nnpm run dev\n\`\`\`\n\nBuilt with Next.js 14, Supabase, Tailwind CSS.\n`
  );

  const buffer = await zip.generateAsync({ type: "arraybuffer" });

  return new NextResponse(buffer, {
    headers: {
      "Content-Type": "application/zip",
      "Content-Disposition": `attachment; filename="${folderName}.zip"`,
    },
  });
}
